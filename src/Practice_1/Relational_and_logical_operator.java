package Practice_1;
public class Relational_and_logical_operator 
{
public static void main(String[] args) 
	{
	boolean a=true;
	boolean b=false;
	if(a&b)
	System.out.println("true");
	else
		System.out.println("false");
	
	if(a||b)
		System.out.println("true");
	else
		System.out.println("false");
	
	if(!a)
		System.out.println("true");
	
	
}
}
